<template>
  <div class="card mb-4" v-bind:class="{pulsate: waiting}">
    <div class="card-body m-0">
      <img class="image"
           v-bind:src="medal.url"
           v-bind:alt="medal.title"
           v-bind:title="medal.title"
      >
      {{medal.title}}
      <div>
        <button class="btn btn-sm btn-outline-success"
                @click="add"
        >
          Adicionar o jogo
        </button>
      </div>
    </div>
  </div>
</template>

<script>
  export default {
    name: 'MedalAdd',
    props: {
      g: { // Game
        type: Object,
        required: true
      },
      m: { // Medal
        type: Object,
        required: true
      }
    },
    data() {
      return {
        waiting: false,
        game: {},
        medal: {}
      };
    },
    methods: {
      add() {
        this.waiting = true;
        this.$auth.$http.post(`game/${this.game.id}/medal`, this.medal)
          .then(response => {
            if (response.data) {
              if (response.data.data) {
                const medal = response.data.data;
                this.$set(this.game.medals, medal.id, medal);
              }
            }
          })
          .catch(error => {
          })
          .finally(() => {
            this.waiting = false;
          });
      }
    },
    mounted() {
      this.game = this.g;
      this.medal = this.m;
    }
  };
</script>

